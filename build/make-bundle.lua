-- build/make-bundle.lua : emit ONE self-contained shen-lua file.
--
--   luajit build/make-bundle.lua            -> build/shen-bundle.lua
--   luajit build/make-bundle.lua out.lua    -> out.lua
--
-- The bundle embeds (a) the Lua module sources, (b) the precompiled kernel
-- as a bytecode-cache blob (the same format boot.lua's kernel cache writes:
-- chunk dumps + ARITY + KDATA), and (c) the .kl sources as a fallback for a
-- LuaJIT whose bytecode format differs from the build machine's. Usage:
--
--   local shen = require("shen-bundle")   -- from any directory
--   shen.boot{quiet=true}
--   print(shen.eval("(+ 1 2)"))           --> 3
--
-- Boot goes through the bytecode path (no .kl files touched), i.e. tens of
-- milliseconds, and needs NO access to the shen-lua checkout.

-- ---- locate the repo root from this script's own path ---------------------
local self = arg and arg[0] or "build/make-bundle.lua"
local root = self:match("^(.*)[/\\]build[/\\][^/\\]+$") or "."
local outpath = arg[1] or (root .. "/build/shen-bundle.lua")

local ffi = require("ffi")
ffi.cdef("int setenv(const char *name, const char *value, int overwrite);")
local function setenv(k, v) ffi.C.setenv(k, v, 1) end

package.path = root .. "/?.lua;" .. package.path

local function read_file(path)
  local fh = assert(io.open(path, "rb"), "cannot open " .. path)
  local s = fh:read("*a"); fh:close()
  return s
end

-- ---- 1. produce a fresh kernel bytecode-cache blob -------------------------
-- Compile the kernel in-process with the cache redirected to a temp file;
-- boot.lua's own write_cache produces exactly the blob format we embed
-- (chunks + ARITY + KDATA), so the bundle and the on-disk cache can never
-- drift apart.
local tmpcache = os.tmpname()
os.remove(tmpcache)
setenv("SHEN_KERNEL_CACHE", tmpcache)
setenv("SHEN_KL_DIR", root .. "/klambda")
setenv("SHEN_FASL", "off")

io.stderr:write("[bundle] compiling kernel ...\n")
local P = require("boot")
P.load_kernel(false)
local blob = read_file(tmpcache)
os.remove(tmpcache)
io.stderr:write(("[bundle] kernel blob: %d bytes\n"):format(#blob))

-- ---- 2. gather sources ------------------------------------------------------
-- Load order at require time is handled by require itself (each module
-- requires its deps); we just need every module preloadable.
local MODULES = {
  "runtime", "compiler", "prims", "boot",
  "prolog_engine", "prolog_compile", "typecheck_native",
  "lua_interop", "repl",
  "shen",
}

-- keep in sync with FILES in boot.lua (embed every .kl so the source
-- fallback can never be missing a file)
local kl_names = {}
do
  local p = io.popen("ls '" .. root .. "/klambda'")
  for line in p:lines() do
    local nm = line:match("^(.+)%.kl$")
    if nm then kl_names[#kl_names + 1] = nm end
  end
  p:close()
  assert(#kl_names >= 21, "klambda/ looks incomplete (" .. #kl_names .. " files)")
end

-- ---- 3. emit ---------------------------------------------------------------
-- One Lua string literal per payload. Escape NUL/controls/backslash/quote
-- (3-digit decimal escapes so a following digit can't extend the escape);
-- bytes >= 128 are legal raw in a Lua literal and stay raw to keep the file
-- close to payload size.
local function quote(s)
  return '"' .. s:gsub('[%z\1-\31\\"\127]', function(c)
    return string.format("\\%03d", string.byte(c))
  end) .. '"'
end

local out = {}
local function emit(s) out[#out + 1] = s end

emit("-- shen-bundle.lua : self-contained shen-lua (generated by build/make-bundle.lua)\n")
emit("-- Shen kernel 41.2 on LuaJIT. require() it and go:\n")
emit("--   local shen = require(\"shen-bundle\"); shen.boot{quiet=true}\n")
emit("--   print(shen.eval(\"(+ 1 2)\"))\n")
emit("-- Generated " .. os.date("!%Y-%m-%dT%H:%M:%SZ") .. " with " .. jit.version .. " (" .. jit.os .. " " .. jit.arch .. ").\n")
emit("-- The kernel ships as precompiled bytecode for that LuaJIT flavour; on a\n")
emit("-- different one, boot falls back to compiling the embedded .kl sources.\n\n")

emit("local sources = {}\n")
for _, m in ipairs(MODULES) do
  emit(("sources[%q] = %s\n"):format(m, quote(read_file(root .. "/" .. m .. ".lua"))))
end

emit("\nlocal kl = {}\n")
for _, nm in ipairs(kl_names) do
  emit(("kl[%q] = %s\n"):format(nm, quote(read_file(root .. "/klambda/" .. nm .. ".kl"))))
end

emit("\nlocal KERNEL = " .. quote(blob) .. "\n")

emit([[

local loadstr = loadstring or load
for name, src in pairs(sources) do
  if package.loaded[name] == nil then
    package.preload[name] = function(...)
      return assert(loadstr(src, "@shen-bundle/" .. name .. ".lua"))(...)
    end
  end
end

local P = require("boot")
P.KERNEL_CACHE_DATA = KERNEL   -- boot.load_kernel() takes the embedded path
P.KL_SOURCES = kl              -- source fallback for a foreign LuaJIT
return require("shen")
]])

local fh = assert(io.open(outpath, "wb"))
fh:write(table.concat(out))
fh:close()
io.stderr:write(("[bundle] wrote %s (%d bytes)\n"):format(outpath, #table.concat(out)))
